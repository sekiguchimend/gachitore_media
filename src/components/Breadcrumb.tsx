import Link from "next/link";

interface BreadcrumbItem {
  label: string;
  href?: string;
}

interface BreadcrumbProps {
  items: BreadcrumbItem[];
}

export function Breadcrumb({ items }: BreadcrumbProps) {
  return (
    <nav className="py-4" aria-label="パンくずリスト">
      <ol className="flex items-center gap-2 text-xs font-mono flex-wrap">
        <li>
          <Link
            href="/"
            className="text-[#666] hover:text-[#00ff88] transition-colors flex items-center gap-1"
          >
            <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M3 12l2-2m0 0l7-7 7 7M5 10v10a1 1 0 001 1h3m10-11l2 2m-2-2v10a1 1 0 01-1 1h-3m-6 0a1 1 0 001-1v-4a1 1 0 011-1h2a1 1 0 011 1v4a1 1 0 001 1m-6 0h6" />
            </svg>
            HOME
          </Link>
        </li>
        {items.map((item, index) => (
          <li key={index} className="flex items-center gap-2">
            <span className="text-[#333]">/</span>
            {item.href ? (
              <Link
                href={item.href}
                className="text-[#666] hover:text-[#00ff88] transition-colors"
              >
                {item.label}
              </Link>
            ) : (
              <span className="text-[#888]">{item.label}</span>
            )}
          </li>
        ))}
      </ol>
    </nav>
  );
}

